    private void btnKhoiPhucActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnKhoiPhucActionPerformed
        // TODO add your handling code here:
        Long selectedIds[] = getSelectedIds();
        try {
            if (JOptionPane.showConfirmDialog(null, "Xác nhận phục hồi ?", "Phục hồi hóa đơn", ERROR_MESSAGE) != YES_OPTION) {
                return;
            }
            for (int i = 0; i < selectedIds.length; i++) {
                Dao.RestoreFromRecycle(selectedIds[i]);
            }
            this.dispose();
            SessionEntity ss = new SessionEntity();
            ss.setUser(userSS);
            ss.setMessage("Khôi phục thông tin phòng trọ đã xóa");
            ss.setStartTime(new Timestamp(System.currentTimeMillis()));
            sessionDao.save(ss);
            SessionEntity sss = sessionDao.getLast(userSS.getId());
            setSession(sss);
        } catch (Exception e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_btnKhoiPhucActionPerformed

    private void btnXoaVinhVienActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaVinhVienActionPerformed
        // TODO add your handling code here:
        Long selectedIds[] = getSelectedIds();
        try {
            if (JOptionPane.showConfirmDialog(null, "Xác nhận xóa vĩnh viễn?", "Xóa hóa đơn", ERROR_MESSAGE) != YES_OPTION) {
                return;
            }
            for (int i = 0; i < selectedIds.length; i++) {
                Dao.deleteById(selectedIds[i]);
            }
            this.dispose();
            SessionEntity ss = new SessionEntity();
            ss.setUser(userSS);
            ss.setMessage("Xóa phòng trọ vĩnh viễn");
            ss.setStartTime(new Timestamp(System.currentTimeMillis()));
            sessionDao.save(ss);
            SessionEntity sss = sessionDao.getLast(userSS.getId());
            setSession(sss);
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Phải xóa khóa ngoại");
        }
    }//GEN-LAST:event_btnXoaVinhVienActionPerformed

