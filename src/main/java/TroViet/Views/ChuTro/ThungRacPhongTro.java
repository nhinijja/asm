    public void fillToTableOrder() {
        tableModel = (DefaultTableModel) tblThungRacPhongTro.getModel();
        tableModel.setRowCount(0);
        try {
            for (PhongTro phongtro : Dao.getAllFromRecycle()) {
                Vector<Object> vec = new Vector<Object>();
                vec.add(phongtro.getId());
                vec.add(phongtro.getIdMaloaiphong());
                vec.add(phongtro.getIdNguoidung());
                vec.add(phongtro.getIdKhuvuc());
                vec.add(phongtro.getTenphong());
                vec.add(phongtro.getDientich());
                vec.add(phongtro.getGiaphong());
                vec.add(phongtro.getDiachi());
                vec.add(phongtro.getMota());
                vec.add(phongtro.getStatus());
                tableModel.addRow(vec);
            }
        } catch (Exception ex) {
            System.out.println(ex);
        }
    }
    
    public Long[] getSelectedIds() {
        int selectedRows[] = tblThungRacPhongTro.getSelectedRows();
        Long selectedIds[] = new Long[selectedRows.length];
        for (int i = 0; i < selectedRows.length; i++) {
            int selectedRow = selectedRows[i];
            Long id = (Long) tblThungRacPhongTro.getValueAt(selectedRow, 0);
            selectedIds[i] = id;
        }
        return selectedIds;
    }

